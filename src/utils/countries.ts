/**
 * Resolves a country code, name, or alpha-3 code to a lowercase ISO alpha-2 code for flags.
 */
export function resolveCountryFlagCode(input: string | undefined | null): string | null {
  if (!input?.trim()) return null;

  const trimmed = input.trim();
  if (trimmed.length === 2) return trimmed.toLowerCase();

  if (trimmed.length === 3) {
    const alpha2 = convertAlpha3ToAlpha2(trimmed);
    if (alpha2) return alpha2;
  }

  const normalized = trimmed.toLowerCase();
  const byName = ALL_COUNTRIES.find((c) => c.name.toLowerCase() === normalized);
  if (byName) return byName.code;

  const byCode = ALL_COUNTRIES.find((c) => c.code === normalized);
  return byCode?.code ?? null;
}

/**
 * Gets the URL for a country flag based on the country code with multiple fallback options
 * @param countryCode - ISO 3166-1 alpha-2 country code or alpha-3 code
 * @param size - Size of the flag image
 * @returns URL to the country flag
 */
export function getFlagUrl(countryCode: string, size: number = 80): string {
  if (!countryCode) return '';
  
  // If it's an alpha-3 code, convert to alpha-2
  if (countryCode.length === 3) {
    countryCode = convertAlpha3ToAlpha2(countryCode) || countryCode;
  }
  
  // Ensure the country code is lowercase for consistency
  const code = countryCode.toLowerCase();
  
  // Use flagcdn.com which is a reliable source for flag images
  return `https://flagcdn.com/w${size}/${code}.png`;
}

/**
 * Gets alternative flag sources if the primary one fails
 * @param countryCode - ISO 3166-1 alpha-2 country code
 * @returns URL to alternative flag source
 */
export function getAlternativeFlagUrl(countryCode: string): string {
  if (!countryCode) return '';
  const code = countryCode.toLowerCase();
  
  // Use flagpedia as an alternative source
  return `https://flagpedia.net/data/flags/w580/${code}.png`;
}

/**
 * Gets flag emoji for a country
 * @param countryCode - ISO 3166-1 alpha-2 country code
 * @returns Flag emoji string
 */
export function getFlagEmoji(countryCode: string): string {
  if (!countryCode) return '';
  
  // Ensure the country code is uppercase for regional indicator symbols
  const codePoints = countryCode
    .toUpperCase()
    .split('')
    .map(char => 127397 + char.charCodeAt(0));
  
  return String.fromCodePoint(...codePoints);
}

/**
 * Converts ISO 3166-1 alpha-3 country code to alpha-2
 * @param alpha3 - The 3-letter country code
 * @returns The corresponding 2-letter country code or null if not found
 */
export function convertAlpha3ToAlpha2(alpha3: string): string | null {
  const code = ISO_ALPHA3_TO_ALPHA2[alpha3.toUpperCase()];
  return code || null;
}

